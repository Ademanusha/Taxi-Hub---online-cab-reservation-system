package com.talentsprint.TaxiHub.model;

public class DriverModel {
	String driver_name;
	String driver_phone_num;
	String registration_num;
	
	public String getDriver_name() {
		return driver_name;
	}
	public void setDriver_name(String driver_name) {
		this.driver_name = driver_name;
	}
	public String getDriver_phone_num() {
		return driver_phone_num;
	}
	public void setDriver_phone_num(String driver_phone_num) {
		this.driver_phone_num = driver_phone_num;
	}
	public String getRegistration_num() {
		return registration_num;
	}
	public void setRegistration_num(String registration_num) {
		this.registration_num = registration_num;
	}
	
}
